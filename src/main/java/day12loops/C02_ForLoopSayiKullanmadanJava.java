package day12loops;

public class C02_ForLoopSayiKullanmadanJava {

    public static void main(String[] args) {

        //Hic rakam kullanmadan 1 den 100 e kadar sayilari konsola yazdiriniz.

        for (int i = 'e' / 'e'; i < 'e'; i++) { //e'nin ascii degeri 101, d'nin ascii degeri 100
            System.out.print(i + "");
        }

    }
}
